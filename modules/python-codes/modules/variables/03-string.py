# String
name = "Rodrigo Leite - Software Engineer"
print(name)
print(type(name))

# [String]
# Esse número abaixo '1000' é do tipo string,
# porque está entre aspas (simples ou duplas).
number_false = '1000'
print(number_false)
print(type(number_false))

# Adição com Strings
text1 = 'Hello '
text2 = "Python!"
mensage = text1 + text2
print(mensage)

# Multiplicação com Strings
text = 'python '
msg = text * 3
print(msg)

# Converte o texto inteiro para maiúsculo.
print(text.upper())

# Converte apenas a primeira letra do texto para maiúsculo.
print(text.capitalize())

